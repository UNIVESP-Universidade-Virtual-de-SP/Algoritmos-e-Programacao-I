"""
Comece executando a atribuição:
s = '0123456789'
Agora, escreva expressões (usando s e o operador de indexação) que
sejam avaliadas como:  
'234'
'78'
'1234567'
'0123'
'789'
"""

s = '0123456789'

print(s[2:5])
    # Output => 234
print(s[7:8])
    # Output => 78
print(s[1:8])
    # Output => 1234567
print(s[0:4])
    # Output => 0123
print(s[7:10])
    # Output => 789
